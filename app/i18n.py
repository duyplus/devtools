def normalize_language(value):
    if value in LANGUAGES:
        return value
    return DEFAULT_LANGUAGE


def translate(language, key):
    return MESSAGES.get(language, MESSAGES[DEFAULT_LANGUAGE]).get(
        key,
        MESSAGES[DEFAULT_LANGUAGE].get(key, key),
    )


def translate_error(language, error):
    message = translate(language, getattr(error, "key", str(error)))
    params = {
        key: translate(language, value) if isinstance(value, str) else value
        for key, value in getattr(error, "params", {}).items()
    }
    try:
        return message.format(**params)
    except (KeyError, ValueError):
        return message
